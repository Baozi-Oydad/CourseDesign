		    public static String getAccnum() {//不使用String，因为需要大量拼接字符串
		    	String zh=new String("2019");//账号前4位相同
		    	StringBuilder zh1=new StringBuilder(String.valueOf(getNum(0,9999)));//随机取后4位
		    	if(zh1.length()==1) {
		    		zh1=zh1.insert(0, "000");//如果是1位数，前面增加3个0
		    		zh=zh+zh1;//前6位与后3位拼接成学号
		    	}else if(zh1.length()==2) {
		    		zh1=zh1.insert(0, "00");//如果是2位数，前面增加2个0
		    		zh=zh+zh1;
		    	}else if(zh1.length()==3) {
		    		zh1=zh1.insert(0, "0");//如果是3位数，前面增加1个0
		    		zh=zh+zh1;
		    	}else {
		    		zh=zh+zh1;
		    	}
		    	return zh;
		    }
