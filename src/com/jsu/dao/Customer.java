			public static boolean isMobileNO(String mobiles) {
		 
				Pattern pattern = Pattern.compile("^((13[0-9])|(15[^4,\\D])|(18[0,5-9]))\\d{8}$");
		 
				Matcher matcher = pattern.matcher(mobiles);
		 
				return matcher.matches();
			}
